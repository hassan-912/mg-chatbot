/**
 * Search the knowledge base for relevant chunks.
 * Uses keyword matching with scoring.
 * @param {string} query - The user's question
 * @param {number} topK - Number of top results to return
 * @returns {Array} - Top matching knowledge chunks
 */
function search(query, topK = 3) {
    const queryLower = query.toLowerCase();
    const queryWords = queryLower.split(/\s+/);

    const scored = knowledgeChunks.map(chunk => {
        let score = 0;

        // Keyword matching
        for (const keyword of chunk.keywords) {
            const kwLower = keyword.toLowerCase();
            if (queryLower.includes(kwLower)) {
                score += 10; // exact substring match in query
            }
            for (const word of queryWords) {
                if (kwLower.includes(word) || word.includes(kwLower)) {
                    score += 3; // partial word match
                }
            }
        }

        // Title matching
        const titleLower = chunk.title.toLowerCase();
        for (const word of queryWords) {
            if (titleLower.includes(word)) {
                score += 5;
            }
        }

        // Content matching (lighter weight)
        for (const word of queryWords) {
            if (word.length > 2 && chunk.content.toLowerCase().includes(word)) {
                score += 1;
            }
        }

        return { ...chunk, score };
    });

    return scored
        .filter(c => c.score > 0)
        .sort((a, b) => b.score - a.score)
        .slice(0, topK);
}

/**
 * Get all available program titles (for listing)
 */
function listPrograms() {
    return knowledgeChunks
        .filter(c => c.id !== "company_consultation")
        .map(c => c.title);
}

